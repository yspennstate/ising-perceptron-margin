def extract_region1_bdef(path, out_dir):
    """Loewner-majorant definiteness per band, when the manifest carries
    exact B-matrix packets (runs after 2026-07-14 do; earlier manifests
    are skipped with a note).  Claims per band, at one common power of
    ten: b11_lo > 0, b22_lo > 0, b11_lo * b22_lo > max|b12 endpoint|^2."""
    data = json.load(open(path))
    packs = [rec.get('bmat_packets') for rec in data['results']]
    if not any(packs):
        print('  note: no bmat_packets in this manifest; B-definiteness '
              'family skipped (pre-packet run)')
        return None
    rows = []
    for rec in data['results']:
        p = rec.get('bmat_packets')
        if p is None:
            raise ValueError('mixed packet presence across bands')
        (m11, r11, e11), (m12, r12, e12), (m22, r22, e22) = \
            (packet(x) for x in p)
        shift = min(e11, e12, e22)
        s11, s12, s22 = (10 ** (e11 - shift), 10 ** (e12 - shift),
                         10 ** (e22 - shift))
        m11, r11 = m11 * s11, r11 * s11
        m12, r12 = m12 * s12, r12 * s12
        m22, r22 = m22 * s22, r22 * s22
        # mirror
        lo11, lo22 = m11 - r11, m22 - r22
        b = max(abs(m12 - r12), abs(m12 + r12))
        if not (lo11 > 0 and lo22 > 0 and b * b < lo11 * lo22):
            raise ValueError('mirror B-definiteness failed')
        rows.append((m11, r11, m22, r22, m12, r12))
    lists = ',\n  '.join(
        f'({lean_int(a)}, {lean_int(b_)}, {lean_int(c)}, {lean_int(d)}, '
        f'{lean_int(e)}, {lean_int(f)})' for a, b_, c, d, e, f in rows)
    body = f'''
def bDefOk : Int × Int × Int × Int × Int × Int → Bool
  | (m11, r11, m22, r22, m12, r12) =>
    let l11 := m11 - r11
    let l22 := m22 - r22
    let b := (max (m12 - r12).natAbs (m12 + r12).natAbs : Int)
    decide (0 < l11) && decide (0 < l22) && decide (b * b < l11 * l22)

def r1BMat : List (Int × Int × Int × Int × Int × Int) := [
  {lists}]

theorem r1_bdef : r1BMat.all bDefOk = true := by decide
'''
    return emit(os.path.join(out_dir, 'R1BDef.lean'), 'R1BDef', body)


def extract_region1_schedule(path, out_dir):
    data = json.load(open(path))
    bands = [tuple(dec_fraction(x) for x in rec['band'])
             for rec in data['results']]
    fails = [rec for rec in data['results'] if not rec.get('ok')]
    if fails:
        raise ValueError(f'{len(fails)} failed bands; not extractable as '
                         f'a certificate')
    levels = {}
    for (t0, t1, th0, th1) in bands:
        levels.setdefault((t0, t1), []).append((th0, th1))
    radii = sorted(levels, key=lambda p: p[0])
    if radii[0][0] != 0:
        raise ValueError('no origin band')
    rad_chain = chain_tuples(radii)
    # per radius level, split the sorted chunks into maximal runs whose
    # seams chain exactly; each run becomes one Lean chainOk list, so the
    # kernel re-decides both the seam equalities and every chunk's
    # nonemptiness.  Runs may not overlap (mirror check).
    runs = []
    n_chunks = 0
    for key in radii:
        arcs = sorted(levels[key])
        n_chunks += len(arcs)
        run = [arcs[0]]
        for (a, b) in arcs[1:]:
            if run[-1][1] == a:
                run.append((a, b))
            elif run[-1][1] > a:
                raise ValueError('overlapping angular chunks')
            else:
                runs.append(run)
                run = [(a, b)]
        runs.append(run)
    run_tuples = [chain_tuples(run) for run in runs]
    run_lists = '\n'.join(
        f'def r1Run{k} : List (Int × Int × Int × Int) := '
        f'{lean_list4(ts)}' for k, ts in enumerate(run_tuples))
    run_thms = '\n'.join(
        f'theorem r1_run{k} : chainOk r1Run{k} = true := by decide'
        for k in range(len(run_tuples)))
    body = f'''
def r1Radii : List (Int × Int × Int × Int) := {lean_list4(rad_chain)}
{run_lists}

theorem r1_radii : chainOk r1Radii = true := by decide
{run_thms}

-- {len(runs)} angular runs over {len(radii)} radius levels,
-- {n_chunks} chunks in all.
'''
    return emit(os.path.join(out_dir, 'R1Schedule.lean'), 'R1', body)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument('--results', default=os.path.join('..', 'results'))
    ap.add_argument('--tag', default='0p05')
    ap.add_argument('--out', default='generated')
    args = ap.parse_args()
    os.makedirs(args.out, exist_ok=True)
    r = args.results
    t = args.tag
    manifest = {'inputs': {}, 'outputs': {}}
    def sweep_rect(p, o):
        pol = json.load(open(p))['policy']
        a1m = dec_fraction(pol['A1MAX'])
        a2m = dec_fraction(pol['A2MAX'])
        return extract_grid_schedule(
            p, o, 'SweepGrid', rect=((-a1m, a1m), (-a2m, a2m)))

    def stage2_rect(p, o):
        pol = json.load(open(p))['policy']
        e = [dec_fraction(x) for x in pol['EXCL_OLD']]
        return extract_grid_schedule(
            p, o, 'Stage2Grid', rect=((e[0], e[1]), (e[2], e[3])))

    jobs = [
        ('huang_star_interior_%s.json' % t, extract_star_interior),
        ('huang_sweep_%s.json' % t, sweep_rect),
        ('huang_region1_%s.json' % t, extract_region1_schedule),
        ('huang_region1_%s.json' % t, extract_region1_bdef),
        ('huang_sweep2_%s.json' % t, stage2_rect),
    ]
    for name, fn in jobs:
        path = os.path.join(r, name)
        if not os.path.exists(path):
            print(f'skip (not present yet): {name}')
            continue
        out_sha = fn(path, args.out)
        if out_sha is None:
            continue
        label = f'{name}:{fn.__name__}'
        manifest['inputs'][label] = sha256_file(path)
        manifest['outputs'][label] = out_sha
        print(f'extracted {label} -> {out_sha[:16]}')
    with open(os.path.join(args.out, 'manifest.json'), 'w',
              encoding='utf-8', newline='\n') as f:
        json.dump(manifest, f, indent=1, sort_keys=True)
    print('wrote', os.path.join(args.out, 'manifest.json'))


if __name__ == '__main__':
    main()
